#!/usr/bin/env node
// Inlines reference/*.md into src/reference-bundle.ts so the same code runs
// on Node (stdio) and Cloudflare Workers without filesystem access.
//
// Re-run via `npm run build` (this is a prebuild hook).

import { readFile, readdir, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const repoReference = resolve(here, "..", "..", "reference");
const out = resolve(here, "..", "src", "reference-bundle.ts");

async function read(...segments) {
  return readFile(resolve(repoReference, ...segments), "utf8");
}

function fileToQuestionId(filename) {
  return filename.replace(/\.md$/, "").replace(/-/g, "_");
}

const commonMistakes = await read("common-mistakes.md");
const successfulPatterns = await read("successful-patterns.md");

const guidanceDir = resolve(repoReference, "question-guidance");
const guidanceFiles = (await readdir(guidanceDir))
  .filter((f) => f.endsWith(".md"))
  .sort();

const guidanceEntries = await Promise.all(
  guidanceFiles.map(async (filename) => {
    const text = await readFile(resolve(guidanceDir, filename), "utf8");
    return [fileToQuestionId(filename), text];
  }),
);

const banner = `// AUTO-GENERATED by scripts/build-reference.mjs. Do not edit by hand.
// Re-run: npm run build (or: node scripts/build-reference.mjs)
`;

const body = `${banner}
export const COMMON_MISTAKES: string = ${JSON.stringify(commonMistakes)};

export const SUCCESSFUL_PATTERNS: string = ${JSON.stringify(successfulPatterns)};

export const QUESTION_GUIDANCE: Record<string, string> = ${JSON.stringify(
  Object.fromEntries(guidanceEntries),
  null,
  2,
)};
`;

await writeFile(out, body, "utf8");
console.log(`wrote ${out} (${guidanceEntries.length} question guidance files)`);
